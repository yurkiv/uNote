package com.yurkiv.materialnotes.model;

public class Hashtag {
    private Long id;
    private String text;

    public Hashtag(String text) {
        this.text = text;
    }

    public Long getId() {
        return id;
    }

    public void setId(Long id) {
        this.id = id;
    }

    public String getText() {
        return text;
    }

    public void setText(String text) {
        this.text = text;
    }
}
